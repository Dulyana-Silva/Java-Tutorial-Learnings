public class Test6 {
    public static void main(String [] args){
        int x = 10;
        int y = -20;

        if (x>0 && y>0)  // AND Operator
              { System.out.println("Positive");
        }
        else {  System.out.println("Negative");
        }

         if (x>0 || y>0)  // OR Operator
              { System.out.println("Positive");
        }
        else {  System.out.println("Negative");
        }

    }

    
}
